export const getWeaponsByRarity = (rarity: CSGOItem['rarity']): CSGOItem[] => {
  return csgoWeapons.filter(weapon => weapon.rarity === rarity);
};

// Helper function to get all rarities
export const getAllRarities = (): CSGOItem['rarity'][] => {
  return ['common', 'uncommon', 'rare', 'epic', 'legendary', 'knife'];
};

// Helper function to get rarity color
export const getRarityColor = (rarity: CSGOItem['rarity']): string => {
  const colors = {
    common: 'from-gray-400 to-gray-500',
    uncommon: 'from-green-400 to-green-500',
    rare: 'from-blue-400 to-blue-500',
    epic: 'from-purple-400 to-purple-500',
    legendary: 'from-yellow-400 to-yellow-500',
    knife: 'from-orange-400 to-red-500',
  };
  return colors[rarity];
};

